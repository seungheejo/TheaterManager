package theater.vo;

public class Theater {
	private String theaterName;
	private int showingRoomSeatsNum;
	
	public Theater() {
		
	}

	public String getTheaterName() {
		return theaterName;
	}

	public void setTheaterName(String theaterName) {
		this.theaterName = theaterName;
	}

	public int getShowingRoomSeatsNum() {
		return showingRoomSeatsNum;
	}

	public void setShowingRoomSeatsNum(int showingRoomSeatsNum) {
		this.showingRoomSeatsNum = showingRoomSeatsNum;
	}
	
	
}
